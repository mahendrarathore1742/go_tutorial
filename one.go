package main

import "fmt"

func main() {

	var msr1 string = "mahendra singh"
	var msr2 string = " Rathore"

	fmt.Println(msr1 + msr2)

	var isLoggedin bool = false
	fmt.Println(isLoggedin)

	var smallvalue uint8 = 100
	fmt.Println(smallvalue >> 1)

	var smallfloat float32 = 105.453
	fmt.Println(smallfloat)

	var bana = "google.com"
	fmt.Println(bana)

	acer := "acer"
	fmt.Println(acer)

}
