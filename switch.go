package main

import "fmt"

func main() {

	value := 32

	switch value {

	case 1:
		fmt.Println("Its is 1")

	case 2:
		fmt.Println("Its is 2")

	case 3:
		fmt.Println("Its is 3")

	case 33:
		fmt.Println("Its is 32")

	case 55:
		fmt.Println("Its is 55")

	default:
		fmt.Println("sorry there is no value but  i always run for this program")

	}

}
